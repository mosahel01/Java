


public class TypeCasting {
    public static void main(String[] args) {

        // Type casting

        int a = (int) 8.5;
        double b = (double) 34;
        float c = (float) 34;
        char d = (char) 34;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }

}






















