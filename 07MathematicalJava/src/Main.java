public class Main {
    public static void main(String[] args) {
        System.out.println("Mathematics Class");


//      That is how we get calculators
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.pow(2, 8));
        System.out.println(Math.abs(-5));   // make it positive
        System.out.println(Math.sqrt(9));
        System.out.println(Math.round(3.24));
        System.out.println(Math.ceil(3.24)); // round up
        System.out.println(Math.floor(3.24)); // round down
        System.out.println(Math.min(10, 21));
        System.out.println(Math.max(10, 21));
    }
}